/*===========================================================================

FUNCTION cmd_malloc

DESCRIPTION

DEPENDENCIES

RETURN VALUE

===========================================================================*/
void * cmd_malloc (uint16 xWantedSize )
{
	uint16 mallocBuffer = 0;

	while( mallocBuffer < xWantedSize )
	{
		mallocBuffer += COMMAND_MIN_BUFFER;
	}

	return pvPortMalloc( mallocBuffer);
}

